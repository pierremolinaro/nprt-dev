//--------------------------------------------------------------------------------------------------

import Foundation

//--------------------------------------------------------------------------------------------------

public enum Spacing : Sendable {
  case zero
  case small
  case regular
  case large

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  var floatValue : CGFloat {
    switch self {
    case .zero    : return  0.0
    case .small   : return  4.0
    case .regular : return  8.0
    case .large   : return 12.0
    }
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

}

