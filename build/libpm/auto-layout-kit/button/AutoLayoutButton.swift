import AppKit

//--------------------------------------------------------------------------------------------------

public class AutoLayoutButton : BaseControl {

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  let mCocoaButton : NSButton

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public init (title inTitle : String, size inSize : NSControl.ControlSize) {
    self.mCocoaButton = InternalNSButton ()
    super.init (cocoaControl: self.mCocoaButton, size: inSize)

    self.mCocoaButton.bezelStyle = .rounded
    self.mCocoaButton.title = inTitle
    self.mCocoaButton.lineBreakMode = .byTruncatingTail
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  Title
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  func setTitle (_ inTitle : String) {
    self.mCocoaButton.title = inTitle
    self.invalidateSimpleLayout ()
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  Action
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  final private var mStateDidChangeAction : Optional < (NSControl.StateValue) -> Void > = nil

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public final func setStateDidChangeAction (_ inAction : @escaping (NSControl.StateValue) -> Void) -> Self {
    self.mStateDidChangeAction = inAction
    return self
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  override func runAction () {
    super.runAction ()
    self.mStateDidChangeAction? (self.mCocoaButton.state)
    if let selector = self.mSelector {
      _ = self.mTarget?.perform (selector)
    }
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   $run binding
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  private var mTarget : AnyObject? = nil
  private var mSelector : Selector? = nil

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public func bind_run (target inTarget : AnyObject, selector inSelector : Selector) -> Self {
    self.mTarget = inTarget
    self.mSelector = inSelector
    return self
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Layout
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public override var hStretchingResistance : HorizontalStretchingResistance { .high }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public override var vAlignment : VerticalAlignmentInHorizontalStack {
    let size = self.smallestSize
    let r = NSRect (origin: .zero, size: size)
    let alignmentRect = self.mCocoaButton.alignmentRect (forFrame: r)
    let baseLine = self.mCocoaButton.lastBaselineOffsetFromBottom + alignmentRect.origin.y
    return .lastBaseline (baseLine)
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public override var vStretchingResistance : VerticalStretchingResistance { .high }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  override public func invalidateSimpleLayout () {
    self.mCocoaButton.invalidateIntrinsicContentSize ()
    super.invalidateSimpleLayout ()
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

}

//--------------------------------------------------------------------------------------------------

